fn main() {
    // Constatnt variable
    let a: i32 = 5;
    let b: i32 = 7;
    let c: i32 = 9;
    let d: i32 = 11;

    // Mutable variable for result
    let mut r: i32;

    // Binary Operations
    r = a + b;
    r = b - c;
    r = c * d;
    r = d / a;
    r = d % c;

    // Bitwise Binary Operations
    r = a & b;
    r = a & b;
    r = b | c;
    r = c ^ d;
    r = !a;
    r = a << b;
    r = a >> b;
}
